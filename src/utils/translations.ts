// Fonction pour traduire un ingrédient
export const translateIngredient = (ingredient: string): string => {
  // Essayer une correspondance exacte
  if (ingredientTranslations[ingredient]) {
    return ingredientTranslations[ingredient];
  }

  // Essayer une correspondance insensible à la casse
  const lowerIngredient = ingredient.toLowerCase();
  for (const [key, value] of Object.entries(ingredientTranslations)) {
    if (key.toLowerCase() === lowerIngredient) {
      return value;
    }
  }

  // Retourner l'original si pas de traduction
  return ingredient;
};

